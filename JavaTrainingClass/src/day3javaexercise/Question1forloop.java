package day3javaexercise;

public class Question1forloop {

	public static void main(String[] args) {
		/*
		 * 1. Write a java program to calculate the sum of first 10 integers. The
		 * program should calculate the sum using for loop. Sum : 55
		 */
		//DataType Variable = Value
		
		int sum = 0;
		for (int i = 0; i <= 10; i++) {
			sum = sum + i;
		}
	System.out.println("Sum: " + sum);
	}
}





